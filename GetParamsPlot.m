function params_plot = GetParamsPlot()
% GetParamsPlot Returns a struct with parameters describing the configuration of a plot
%
%   See also ConfigurePlot

params_plot.alw = 0.75;    % Axes line width
params_plot.fsz = 10;      % Font size
params_plot.lw = 1.5;      % Line width
params_plot.msz = 8;       % Marker size
params_plot.BoxDim = [10,5];
params_plot.font_name = 'Times New Roman';
end